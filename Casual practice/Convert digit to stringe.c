#include<stdio.h>
int main(){
    int number;
    printf("The Number\n", number);
    scanf("%d", &number);
    if (number == 0){
        printf("Zero\n",number);
    }
    else if (number == 1){
        printf("One\n",number);
    }
    else if (number == 2){
        printf("Two\n",number);
    }
    else if (number == 3){
        printf("Three\n",number);
    }
    else if (number == 4){
        printf("Four\n",number);
    }
    else if (number == 5){
        printf("Five\n",number);
    }
    else if (number == 6){
        printf("Six\n",number);
    }
    else if (number == 7){
        printf("Seven\n",number);
    }
    else if (number == 8){
        printf("Eight\n",number);
    }
    else
        printf("Nine\n",number);

return 0;

}
